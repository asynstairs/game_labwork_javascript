let count_players = 0

socket.on('step', function(data) {
    if (data.step) {
        if (data.player === 1) {
            if (data.attack) {
                document.querySelector('#cell_' + data.x + data.y).innerHTML = '(X)';
            } else {
                document.querySelector('#cell_' + data.x + data.y).innerHTML = 'x';
            }
        } else {
            if (data.attack) {
                document.querySelector('#cell_' + data.x + data.y).innerHTML = '(O)';
            } else {
                document.querySelector('#cell_' + data.x + data.y).innerHTML = 'O';
            }
        }
    }
})


